"""
Merge v5i (26-point) and converted v15i (26-point) datasets into a unified
training dataset for YOLO26-Pose pitch keypoint detection.

Run convert_v15i_to_26pt.py first to produce the converted v15i labels.

Usage:
    python training/scripts/merge_datasets.py \
        --v5i  "datasets/football pitch keypoints.v5i.yolov8" \
        --v15i datasets/v15i_converted_26pt \
        --output datasets/pitch_keypoints_26pt
"""

import argparse
import shutil
from pathlib import Path


NUM_KPT = 26
EXPECTED_FIELDS = 5 + NUM_KPT * 3  # 83


def _copy_split(
    src_dir: Path,
    dst_dir: Path,
    prefix: str,
    split: str,
) -> tuple[int, int]:
    """
    Copy images and labels from one dataset split into the merged destination.

    v5i uses 'valid', merged dataset uses 'val' — both are handled.
    Returns (images_copied, labels_copied).
    """
    # Both datasets use 'valid' (Roboflow default); merged uses 'val'
    src_images = src_dir / split / "images"
    src_labels = src_dir / split / "labels"

    dst_images = dst_dir / "images" / _dst_split(split)
    dst_labels = dst_dir / "labels" / _dst_split(split)

    dst_images.mkdir(parents=True, exist_ok=True)
    dst_labels.mkdir(parents=True, exist_ok=True)

    if not src_images.exists():
        return 0, 0

    imgs, lbls = 0, 0

    for img_file in sorted(src_images.iterdir()):
        if img_file.suffix.lower() not in {".jpg", ".jpeg", ".png", ".bmp"}:
            continue
        dst_name = prefix + img_file.name
        shutil.copy2(img_file, dst_images / dst_name)
        imgs += 1

    for label_file in sorted(src_labels.glob("*.txt")):
        # Validate field count
        text = label_file.read_text().strip()
        lines_ok = []
        for line in text.splitlines():
            parts = line.split()
            if len(parts) == EXPECTED_FIELDS:
                lines_ok.append(line)
            else:
                print(f"  Warning: {label_file.name} line has {len(parts)} fields "
                      f"(expected {EXPECTED_FIELDS}), skipping")

        if lines_ok:
            dst_name = prefix + label_file.name
            (dst_labels / dst_name).write_text("\n".join(lines_ok) + "\n")
            lbls += 1

    return imgs, lbls


def _dst_split(split: str) -> str:
    """Normalise split name: 'valid' -> 'val'."""
    return "val" if split == "valid" else split


def _write_data_yaml(out_dir: Path, totals: dict[str, list[int]]) -> None:
    """Write data.yaml into the merged dataset root directory."""
    yaml_content = """\
# Football Pitch Keypoints — 26-point unified dataset
# Merged from v5i (26-pt native) and v15i (converted from 48-pt)
# Generated by training/scripts/merge_datasets.py

path: .
train: images/train
val:   images/val
test:  images/test

kpt_shape: [26, 3]

# Horizontal flip symmetry (see training/configs/pitch_kpt_26.yaml for details)
flip_idx: [
  2, 1, 0,
  13, 12, 11, 10,
  9, 8, 7,
  6, 5, 4, 3,
  16, 17,
  14, 15,
  20, 21,
  18, 19,
  22,
  25, 24, 23
]

nc: 1
names:
  0: football-pitch
"""
    yaml_path = out_dir / "data.yaml"
    yaml_path.write_text(yaml_content)

    train_imgs = totals.get("train", [0])[0]
    val_imgs   = totals.get("val",   [0])[0]
    test_imgs  = totals.get("test",  [0])[0]
    print(f"  data.yaml written  (train={train_imgs}, val={val_imgs}, test={test_imgs})")


def main() -> None:
    parser = argparse.ArgumentParser(description="Merge v5i and converted v15i datasets")
    parser.add_argument("--v5i", type=str,
                        default="datasets/football pitch keypoints.v5i.yolov8",
                        help="Path to v5i dataset (already in 26-point format)")
    parser.add_argument("--v15i", type=str,
                        default="datasets/v15i_converted_26pt",
                        help="Path to converted v15i dataset (output of convert_v15i_to_26pt.py)")
    parser.add_argument("--output", type=str,
                        default="datasets/pitch_keypoints_26pt",
                        help="Output directory for merged dataset")
    args = parser.parse_args()

    v5i_dir = Path(args.v5i)
    v15i_dir = Path(args.v15i)
    out_dir = Path(args.output)

    if not v5i_dir.exists():
        raise SystemExit(f"v5i dataset not found: {v5i_dir}")
    if not v15i_dir.exists():
        raise SystemExit(f"Converted v15i dataset not found: {v15i_dir}\n"
                         "Run convert_v15i_to_26pt.py first.")

    print(f"Merging datasets -> {out_dir}\n")

    totals: dict[str, list[int]] = {"train": [0, 0], "val": [0, 0], "test": [0, 0]}

    for split in ["train", "valid", "test"]:
        key = _dst_split(split)

        imgs_v5, lbls_v5 = _copy_split(v5i_dir, out_dir, "v5_", split)
        imgs_v15, lbls_v15 = _copy_split(v15i_dir, out_dir, "v15_", split)

        totals[key][0] += imgs_v5 + imgs_v15
        totals[key][1] += lbls_v5 + lbls_v15

        print(f"  {key:5s}:  v5i={imgs_v5} imgs + v15i={imgs_v15} imgs  "
              f"| labels: v5i={lbls_v5} + v15i={lbls_v15}")

    print()
    total_imgs = sum(v[0] for v in totals.values())
    total_lbls = sum(v[1] for v in totals.values())
    print(f"Total: {total_imgs} images, {total_lbls} label files")

    # Write data.yaml into the merged dataset directory
    _write_data_yaml(out_dir, totals)

    print()
    print(f"Dataset ready at: {out_dir.resolve()}")
    print("Next: train with  training/configs/pitch_kpt_26.yaml")


if __name__ == "__main__":
    main()
